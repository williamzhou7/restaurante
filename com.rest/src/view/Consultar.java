    private void buscarcliente(){
        readJTable();
    }
    
    private void excluircliente(){
        ClienteDAO cdao = new ClienteDAO();
        cdao.remove((int) consultaTable.getValueAt(consultaTable.getSelectedRow(), 0));
        limpa();
        readJTable();
    }
    
    private void atualizacliente() {
        Cliente c = new Cliente();
        ClienteDAO cdao = new ClienteDAO();
       
        c.setNome_cliente(nomeTextField.getText());
        
        
        cdao.update(c);
        limpa();
        readJTable();
    }
    
    private void limpa(){
        
        nomeTextField.setText(null);
        
        
    }
