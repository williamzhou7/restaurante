    private void Cadastrarmesa(){
        
        
        Mesa m = new Mesa();
        MesaDAO mdao = new MesaDAO();
        
        m.setNome_mesa(Integer.parseInt(mesaTextField.getText()));
        
        mdao.save(m);
        
    }
    
    private void buscar(){
        
        readJTable();
    }
    
        private void verificadigitacao(){
        
        if(mesaTextField.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Favor informar a mesa!");
            cadastrarBtn.requestFocus();
            return;
        }
        
        Cadastrarmesa();
    }
    
