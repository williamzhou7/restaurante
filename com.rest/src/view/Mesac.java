         public void readJTable() {

        DefaultTableModel modelo = (DefaultTableModel) mesaTable.getModel();
        modelo.setNumRows(0);
        ClienteDAO cdao = new ClienteDAO();
        Mesa m = (Mesa) mesaComboBox.getSelectedItem();
        for (Cliente c : cdao.Findmesa(m.getNome_mesa())) {
            modelo.addRow(new Object[]{
                c.getId_cliente(),
                c.getNome_cliente(),
                c.getMesa(),
                c.getObs()
            });
        }
    }

